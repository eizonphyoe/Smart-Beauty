package com.smartbeauty.ucsm.smartbeauty.modle;

public class FirstActivityList {
    int img;
    String type;

    public FirstActivityList(int img, String type) {
        this.img = img;
        this.type = type;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}

